    //metodo cronometro que actua como un reloj actualizando los labels de hora, minuto y segundo
    public class cronometro implements ActionListener{
        @Override
        public void actionPerformed(ActionEvent evt){
            GregorianCalendar tiempo = new GregorianCalendar();
            int hora,minutos,segundos;
            hora=tiempo.get(Calendar.HOUR);
            minutos=tiempo.get(Calendar.MINUTE);
            segundos=tiempo.get(Calendar.SECOND);
            lbhora.setText(String.valueOf(hora));
            lbminuto.setText(String.valueOf(minutos));
            lbsegundo.setText(String.valueOf(segundos));
        }
    }

